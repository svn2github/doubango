int tsip_header_To_tostring(const void* header, tsk_buffer_t* output)
{
	if(header)
	{
		int ret;
		const tsip_header_To_t *To = header;
		if(ret=tsip_uri_tostring(To->uri, 1, 1, output))
		{
			return ret;
		}
		if(To->tag)
		{
			tsk_buffer_appendEx(output, ";tag=%s", To->tag);
		}
	}
	return -1;
}

